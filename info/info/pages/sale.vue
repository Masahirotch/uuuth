<script>
import { mapState , mapGetters, mapActions } from 'vuex'
import axios from 'axios'
export default {
  computed: {
      ...mapGetters( 'user' , [ 'userProfile' ]),

  },
  data() {
    return {
      setUP : false,
    }
  },
  mounted(){

    this.initializeLiff()

  },
  methods:{
    ...mapActions('user',[ 'setProfile' ]),
      ////////////////////////////////////////////////////////
      initializeLiff() {
        liff.init({ liffId: process.env.SALE_LIFF_ID })
          .then( async() => { await this.checkLogin(); })
          .catch((err) => { console.log(err.code, err.message); });
      },
      async checkLogin() {
        if ( !liff.isLoggedIn()) { liff.login(); } 
        else { this.getProfile(); }
      },
      getProfile() {
        liff.getProfile().then( async (profile) => {
              this.setProfile(profile)
        })
        .then( function() { 
          this.redirect()
        }.bind(this))
        .catch((err) => { console.log(err.code, err.message); })
        .finally(function(){
          
          
        }.bind(this))
      
      },
      redirect : function(){
        if( this.$route.query['liff.state'] != undefined ){
          this.$router.push( this.$route.query['liff.state'] )
        }
        this.setUP = true
      },





  },


};
</script>
